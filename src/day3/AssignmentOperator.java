package day3;

public class AssignmentOperator {

	public static void main(String[] args) {
	
		//increment more values +=
		
		//1 Scenario 1 -want to increase a value by 5
		int a=10; //= is assignment operator
		a+=5; // a+=5 equal to a=a+5;
		System.out.println(a); //15
		
		//decrement more values -=
		
		//2 Scenario 2- want to decrease a value by 5
		a-=5; //a-=5 equal to a=a-5;
		System.out.println(a); //10
		
		//3 Scenario3- (multiplication)*=
		int x=5;
		x*=2;//x*=2 equal to x=x*2;
        System.out.println(x); //10
        
        //4 Scenario4-(Division-Quotient)/=
        int z=5;
        z/=2; // z/=2 equal to z=z/2;
        System.out.println(z); //2
        
        //5 Scenario5-Remainder
        int p=5;
        p%=2; //p%=2 equal to p=p%2;
        System.out.println(); //1
        
		
		
		
		
		
		

	}

}
