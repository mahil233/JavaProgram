package day4;

public class EvenOrOddNumber {

	public static void main(String[] args) {
		
		int num=10;
		
		//=(single equal to is for assignment & double equal(==) to is for comparison)
		if(num%2==0)//if condition is true then below statement is executed
		{
			System.out.println("Number is Even");
		}
		
		else
		{
			System.out.println("Number is Odd");
		}

	}

}
